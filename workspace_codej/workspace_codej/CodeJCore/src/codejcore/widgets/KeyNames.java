package codejcore.widgets;

/**
 * Constants relating to keyboard event names on the web client. To be expanded
 * in the future.
 * 
 * @author tgreen
 *
 */
public class KeyNames {

	/**
	 * Default Constructor
	 */
	public KeyNames() {
		// Empty
	}

	/**
	 * Keyboard event name for the Enter key
	 */
	public static final String ENTER_KEY = "Enter";

	/**
	 * Keyboard event name for the Spacebar
	 */
	public static final String SPACE_KEY = "Space";

	/**
	 * Keyboard event name for the Right-Arrow key
	 */
	public static final String ARROW_RIGHT_KEY = "ArrowRight";

	/**
	 * Keyboard event name for the Left-Arrow key
	 */
	public static final String ARROW_LEFT_KEY = "ArrowLeft";

	/**
	 * Keyboard event name for the Up-Arrow key
	 */
	public static final String ARROW_UP_KEY = "ArrowUp";

	/**
	 * Keyboard event name for the Down-Arrow key
	 */
	public static final String ARROW_DOWN_KEY = "ArrowDown";

}
